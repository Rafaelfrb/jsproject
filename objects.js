//Reference types in JavaScript:
//Objects
//Arrays
//Functions

//Example of Object:

let person = {
    name: 'Rafael',
    age: 32
};

//Dot notation - one way of changing a property of an Object

person.name = 'Nicolas';

//Bracket notation - other way

person['name'] = 'Rafael';

//other example of how Bracket Notation can be useful:

let attribute = 'name';
person[attribute] = 'Rafael';